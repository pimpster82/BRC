/**
 * Helper functions for One Year Reading Plan
 */

/**
 * Get all readings in a section
 */
export const getReadingsInSection = (sectionKey) => {
  return oneyearReadings.filter(reading => reading.section === sectionKey)
}

/**
 * Get section for a specific reading ID
 */
export const getSectionForReading = (readingId) => {
  const reading = oneyearReadings.find(r => r.id === readingId)
  return reading ? reading.section : null
}

/**
 * Get completed readings from localStorage
 */
const getCompletedReadings = () => {
  try {
    const stored = localStorage.getItem('bibleCompanion_oneyear')
    if (!stored) return []
    const data = JSON.parse(stored)
    return data.completedReadings || []
  } catch (error) {
    console.error('Failed to load One Year data:', error)
    return []
  }
}

/**
 * Save completed readings to localStorage
 */
const saveCompletedReadings = (completedReadings) => {
  try {
    const data = {
      completedReadings,
      lastUpdated: new Date().toISOString()
    }
    localStorage.setItem('bibleCompanion_oneyear', JSON.stringify(data))
  } catch (error) {
    console.error('Failed to save One Year data:', error)
  }
}

/**
 * Check if a reading is completed
 * @param {number} readingId - Reading ID
 */
export const isReadingCompleted = (readingId) => {
  const completed = getCompletedReadings()
  return completed.includes(readingId)
}

/**
 * Mark a reading as completed
 * @param {number} readingId - Reading ID
 */
export const markReadingComplete = (readingId) => {
  const completed = getCompletedReadings()
  if (!completed.includes(readingId)) {
    completed.push(readingId)
    saveCompletedReadings(completed)
  }
}

/**
 * Unmark a reading as completed
 * @param {number} readingId - Reading ID
 */
export const unmarkReadingComplete = (readingId) => {
  const completed = getCompletedReadings()
  const filtered = completed.filter(id => id !== readingId)
  saveCompletedReadings(filtered)
}

/**
 * Get progress for One Year plan
 */
export const getOneyearProgress = () => {
  const completedReadings = getCompletedReadings()
  const total = oneyearReadings.length
  const completed = completedReadings.length
  const percentage = total > 0 ? Math.round((completed / total) * 100) : 0

  return {
    total,
    completed,
    percentage
  }
}
